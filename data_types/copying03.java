class copying03
{
    String Name;
    int Age;
    copying03(String Name,int Age)
    {
        Name = Name;
        Age = Age;

    }
    public static void main(String [] args)
    {
        copying03 c1 = new copying03("sandeep",25);
        System.out.println(c1.Name);
        System.out.println(c1.Age);
    }
}

      
